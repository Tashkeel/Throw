/// <summary>
/// Rarity tier for shop items (modifiers and enhancements).
/// Controls the probability of being drawn in the shop.
/// </summary>
public enum ItemRarity
{
    /// <summary>50% draw chance.</summary>
    Common,

    /// <summary>30% draw chance.</summary>
    Uncommon,

    /// <summary>15% draw chance.</summary>
    Rare,

    /// <summary>5% draw chance.</summary>
    Legendary
}
